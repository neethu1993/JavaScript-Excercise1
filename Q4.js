/**
 * JavaScript program to sort strings items of an array in both alphabetically ascending and descending order
 */

//Data
var strings = ["Banana", "Orange", "Apple", "Mango"];

//Sorting in ascending order
strings.sort();
console.log("Ascending order: " + strings);

//Sorting in descending order
strings.reverse();
console.log("Descending order: " + strings);